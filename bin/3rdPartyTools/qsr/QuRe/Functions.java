	public static double stdev (double [] a)
	{
		double avg = average(a);
		double std = 0;
		for (int i=0; i<a.length; i++)
			std+=(a[i]-avg)*(a[i]-avg);
		std=std/(a.length-1);
		return Math.sqrt(std);
	}
	
